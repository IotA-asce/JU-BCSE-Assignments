CREATE TABLE DEPARTMENT(
    DEPT_CODE VARCHAR(5) PRIMARY KEY,
    DEPT_NAME CHAR(20)
);

CREATE TABLE EMP(
    EMP_CODE VARCHAR(5) PRIMARY KEY,
    DEPT_CODE VARCHAR(5),
    EMP_NAME CHAR(20) CHECK (EMP_NAME = UPPER(EMP_NAME)),
    ADDRESS CHAR(20),
    CITY CHAR(20),
    BASIC INT NOT NULL CHECK (
        BASIC >= 5000
        AND BASIC <= 9000
    ),
    GRADE CHAR(1) NOT NULL CHECK (
        GRADE = 'A'
        OR GRADE = 'B'
        OR GRADE = 'C'
    ),
    DT_JN TIMESTAMP DEFAULT CURRENT_TIMESTAMP
    FOREIGN KEY (DEPT_CODE) REFERENCES DEPARTMENT(EMP_CODE)
);

CREATE TABLE LEAVE_REGISTER (
    ENTRY_CODE VARCHAR(5) PRIMARY KEY,
    EMP_CODE VARCHAR(5),
    LEAVE_TYPE CHAR(2) NOT NULL CHECK(
        LEAVE_TYPE = 'CL'
        OR LEAVE_TYPE = 'EL'
        OR LEAVE_TYPE = 'ML'
    ),
    FROM_DATE DATE,
    TO_DATE DATE
    -- FOREIGN KEY (EMP_CODE) REFERENCES EMP(ENTRY_CODE) ON DELETE CASCADE
);
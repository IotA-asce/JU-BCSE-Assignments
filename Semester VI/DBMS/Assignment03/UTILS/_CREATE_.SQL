CREATE TABLE DEPARTMENT(
    DEPT_CODE       VARCHAR(5) PRIMARY KEY,
    DEPT_NAME       CHAR(20)
);

CREATE TABLE EMP(
    EMP_CODE        VARCHAR(5),
    EMP_NAME        CHAR(20)    CHECK (EMP_NAME=UPPER(EMP_NAME)),
    ADDRESS         CHAR(20),
    CITY            CHAR(20),
    BASIC           INT         CHECK (BASIC >= 5000 AND BASIC <= 9000),
    GRADE           CHAR(1)     CHECK (GRADE='A' OR GRADE='B' OR GRADE='C'),
    DT_JN           TIMESTAMP   DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY(EMP_CODE)
);

CREATE TABLE LEAVE_REGISTER (
    EMP_CODE        VARCHAR(5)  PRIMARY KEY,
    LEAVE_TYPE      CHAR(2) CHECK(LEAVE_TYPE='CL' OR LEAVE_TYPE='EL' OR LEAVE_TYPE='ML'),
    DT_LV           DATE
);